package org.example.theme;

public class Theme {
    public String darkBackground;
    public String mediumBackground;
    public String lightBackground;
    public String accentColor;
    public String buttonColor;

    public Theme(String darkBackground, String mediumBackground, String lightBackground, String accentColor, String buttonColor) {
        this.darkBackground = darkBackground;
        this.mediumBackground = mediumBackground;
        this.lightBackground = lightBackground;
        this.accentColor = accentColor;
        this.buttonColor = buttonColor;
    }
}
